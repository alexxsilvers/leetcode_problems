func sampleStats(count []int) []float64 {
	min, max, mode, modeCnt, sum, cnt := 255, 0, 0, 0, 0, 0
	for i, k := range count {
		if k == 0 {
			continue
		}
		if i > max {
			max = i
		}
		if i < min {
			min = i
		}
		if k > modeCnt {
			mode, modeCnt = i, k
		}
		sum += i * k
		cnt += k
	}
	result := []float64{float64(min), float64(max), float64(sum) / float64(cnt), 0, float64(mode)}

	outerCnt := 0
	halfCnt := cnt / 2
	if cnt&1 != 0 { // odd
		for i, k := range count {
			outerCnt += k

			if outerCnt > halfCnt {
				result[3] = float64(i)
				break
			}
		}
	} else { // even
		for i, k := range count {
			outerCnt += k

			if outerCnt > halfCnt && outerCnt > halfCnt+1 {
				result[3] = float64(i)
				break
			} else if outerCnt >= halfCnt {
				result[3] = float64(i+i+1) / 2
				break
			}
		}
	}

	return result
}
